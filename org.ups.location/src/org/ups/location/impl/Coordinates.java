package org.ups.location.impl;

public class Coordinates {
	public float latitude = 666;
	public float longitude = 666;
	
	public Coordinates(float latitude, float longitude) {
		super();
		this.latitude = latitude;
		this.longitude = longitude;
	}
	
	public Coordinates() {
		this.latitude = 666;
		this.longitude = 666;
	}
}
